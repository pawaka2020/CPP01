#include "Zombie.hpp"

//allocates an instance of 'Zombie' class on the heap
//a user MUST delete this heap instance before ending a program.
Zombie* newZombie(const std::string& name)
{
	Zombie *zombie = new Zombie();
	zombie->setName(name);
	return zombie;
}